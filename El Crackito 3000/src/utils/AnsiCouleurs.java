package utils;

public class AnsiCouleurs {
    // Codes ANSI pour les couleurs
    public static final String BLANC = "\u001B[0m";
    public static final String ROUGE = "\u001B[31m";
    public static final String VERT = "\u001B[32m";
    public static final String JAUNE = "\u001B[33m";
    public static final String BLEU = "\u001B[34m";
    public static final String ORANGE = "\u001B[36m";
    public static final String CYAN_CLAIR = "\u001B[96m";
}
